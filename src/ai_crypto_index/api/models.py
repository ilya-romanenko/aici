        if normalized is None:
            return None
        lowered = normalized.lower()
        if lowered not in _CTA_EVENT_TYPES:
            raise ValueError(f"event_type must be one of: {', '.join(sorted(_CTA_EVENT_TYPES))}.")
        return lowered


