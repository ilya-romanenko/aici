class SupportTicketAttachment(BaseModel):
    filename: str = Field(..., min_length=1, max_length=160)
    content_type: str | None = Field(default=None, max_length=120)
    content_base64: str = Field(..., min_length=4, max_length=12_000_000)

    @field_validator("filename", mode="before")
    @classmethod
    def _normalize_filename(cls, value: object | None) -> str:
        if value is None:
            raise ValueError("filename is required.")
        return str(value).strip()

    @model_validator(mode="after")
    def _validate_bytes(self) -> "SupportTicketAttachment":
        try:
            data = base64.b64decode(self.content_base64, validate=True)
        except (binascii.Error, ValueError) as exc:
            raise ValueError("Attachment content must be valid base64.") from exc
        if len(data) > SUPPORT_ATTACHMENT_MAX_BYTES:
            max_mb = round(SUPPORT_ATTACHMENT_MAX_BYTES / (1024 * 1024))
            raise ValueError(f"Attachments must be {max_mb} MB or smaller.")
        return self


