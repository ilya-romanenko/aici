def _coerce_string_sequence(value: object | None) -> list[str] | None:
    if value is None:
        return None
    if isinstance(value, str):
        raw_values = value.split(",")
    elif isinstance(value, (list, tuple, set)):
        raw_values = value
    else:
        return None
    cleaned: list[str] = []
    for item in raw_values:
        candidate = str(item).strip()
        if not candidate:
            continue
        cleaned.append(candidate)
        if len(cleaned) >= 32:
            break
    return cleaned or None


