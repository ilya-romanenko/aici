class RunRequest(BaseModel):
    n_top_coins: int = Field(100, ge=RUN_N_TOP_COINS_MIN, le=RUN_N_TOP_COINS_MAX)
    start_date: str | None = Field(
        default=None,
        description="Optional start date in YYYY-MM-DD format.",
        examples=["2023-01-01"],
    )
    lookback_days: int = Field(180, ge=RUN_LOOKBACK_DAYS_MIN, le=RUN_LOOKBACK_DAYS_MAX)
    window_size: int = Field(30, ge=RUN_WINDOW_SIZE_MIN, le=RUN_WINDOW_SIZE_MAX)
    forecast_horizon: int = Field(30, ge=RUN_FORECAST_HORIZON_MIN, le=RUN_FORECAST_HORIZON_MAX)
    advanced_forecast: bool = True
    fresh_data: bool = False
    info_messages: bool = False
    visualization: bool = False
    total_assets: int = Field(10, ge=RUN_TOTAL_ASSETS_MIN, le=RUN_TOTAL_ASSETS_MAX)
    clustering_metric: str = Field(
        "sharpe",
        min_length=1,
        max_length=64,
        description="Metric used inside select_assets_balanced (e.g., 'sharpe').",
    )
    risk_min_weight: float = Field(0.03, ge=RUN_RISK_MIN_WEIGHT_MIN, le=RUN_RISK_MIN_WEIGHT_MAX)
    risk_max_weight: float = Field(0.25, ge=RUN_RISK_MAX_WEIGHT_MIN, le=RUN_RISK_MAX_WEIGHT_MAX)
    weight_cap: float = Field(0.15, ge=RUN_WEIGHT_CAP_MIN, le=RUN_WEIGHT_CAP_MAX)
    vol_floor_ratio: float = Field(0.4, ge=RUN_VOL_FLOOR_RATIO_MIN, le=RUN_VOL_FLOOR_RATIO_MAX)
    gating_tolerance: float = Field(0.02, ge=0.0, le=RUN_GATING_TOLERANCE_MAX)
    run_id: str | None = Field(
        default=None,
        min_length=3,
        max_length=64,
        pattern=r"^[A-Za-z0-9_.\-]+$",
        description="Optional identifier for the run; autogenerated when omitted.",
    )

    @field_validator("start_date", mode="before")
    @classmethod
    def _ensure_valid_start_date(cls, value: object | None) -> str | None:
        if value is None:
            return None
        if isinstance(value, date):
            return value.strftime("%Y-%m-%d")
        if isinstance(value, datetime):
            return value.date().strftime("%Y-%m-%d")
        if isinstance(value, str):
            stripped = value.strip()
            if not stripped or stripped.lower() == "string":
                return None
            try:
                datetime.strptime(stripped, "%Y-%m-%d")
            except ValueError as exc:
                raise ValueError("start_date must match YYYY-MM-DD") from exc
            return stripped
        raise TypeError("start_date must be a date or ISO string")

    @field_validator("advanced_forecast", "fresh_data", "info_messages", "visualization", mode="before")
    @classmethod
    def _normalize_boolean(cls, value: object) -> bool:
        if isinstance(value, bool):
            return value
        if isinstance(value, str):
            lowered = value.strip().lower()
            if lowered in _BOOLEAN_TRUTHY:
                return True
            if lowered in _BOOLEAN_FALSY:
                return False
        raise ValueError("Boolean field must be true/false.")

    @field_validator("run_id", mode="before")
    @classmethod
    def _trim_run_id(cls, value: object | None) -> str | None:
        if value is None:
            return None
        if isinstance(value, str):
            stripped = value.strip()
            return stripped or None
        raise TypeError("run_id must be a string.")

    @model_validator(mode="after")
    def _validate_constraints(self) -> RunRequest:
        if self.risk_max_weight < self.risk_min_weight:
            raise ValueError("risk_max_weight must be greater or equal to risk_min_weight.")
        if self.total_assets > self.n_top_coins:
            raise ValueError("total_assets must be less or equal to n_top_coins.")
        if self.risk_min_weight * self.total_assets > 1.0 + 1e-9:
            raise ValueError("risk_min_weight too high for the requested asset count (sum would exceed 1).")
        risk_min_weight_max = min(RUN_RISK_MIN_WEIGHT_MAX, 1.0 / self.total_assets)
        if self.risk_min_weight > risk_min_weight_max + 1e-9:
            raise ValueError(
                "risk_min_weight exceeds the allowed maximum for the requested asset count."
            )
        weight_cap_min = max(RUN_WEIGHT_CAP_MIN, 1.0 / self.total_assets)
        if self.weight_cap < weight_cap_min - 1e-9:
            raise ValueError("weight_cap must be at least max(0.08, 1 / total_assets).")
        if self.lookback_days < self.window_size:
            raise ValueError("lookback_days must be greater or equal to window_size.")
        if self.forecast_horizon > self.window_size:
            raise ValueError("forecast_horizon must be less or equal to window_size.")
        if self.forecast_horizon > self.lookback_days:
            raise ValueError("forecast_horizon must be less or equal to lookback_days.")
        if self.gating_tolerance > RUN_GATING_TOLERANCE_MAX:
            raise ValueError("gating_tolerance must be 0.10 or less.")
        if self.run_id:
            if self.run_id[0] in {".", "-", "_"}:
                raise ValueError("run_id cannot start with '.', '-', or '_'.")

        if self.start_date:
            parsed = datetime.strptime(self.start_date, "%Y-%m-%d").date()
            if parsed > date.today():
                raise ValueError("start_date cannot be in the future.")
            if parsed < RUN_START_DATE_MIN:
                raise ValueError(f"start_date cannot be earlier than {RUN_START_DATE_MIN.isoformat()}")
            latest_allowed = date.today() - timedelta(days=max(self.lookback_days, RUN_START_DATE_MAX_LOOKBACK_DAYS))
            if parsed > latest_allowed:
                raise ValueError("start_date is too recent for the requested history window.")
            window_start = parsed - timedelta(days=self.lookback_days)
            if window_start < EARLIEST_DATA_DATE:
                raise ValueError(
                    f"lookback_days window exceeds available history from {EARLIEST_DATA_DATE.isoformat()}."
                )
        return self


